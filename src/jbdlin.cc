// for completeness and testing purposes the power series
dcomplex jbdlinpow(int n, dcomplex z){

  dcomplex aa[157],zn=1.;
//std::cout << zn << '\n';
  const int nmax = 156;
  for(int k=1; k <= nmax; k++){
    zn *= z;
    aa[k]=zn/pow(double(k),n);
   }
  dcomplex result=0.;
  for(int k=nmax; k > 0; k--){
    result += aa[k];
  }
//for(int k=1; k<=nmax; k++){
//std:: cout << aa[k] <<'\n';
//}
  return result;
}
